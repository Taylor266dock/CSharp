using System;
using System.Collections;

namespace Demo
{
    class Program
    {
        static void Main(string[]args)
        {
            //Integer
            int a = 1;
            //Float
            float b = 1.1F;
            //String
            string c = "Hello";
            //Character
            char d = 'A';
            //Boolean
            bool e = true;
            //Double
            double f = 1.11111111;

            Console.WriteLine("The integer data type is " + a);

            Console.WriteLine("The float data type is " + b);

            Console.WriteLine("The string data type is " + c);

            Console.WriteLine("The character data type is " + d);

            Console.WriteLine("The boolean data type is " + e);

            Console.WriteLine("The double data type is " + f);

            Console.Read();
        }
    }
}